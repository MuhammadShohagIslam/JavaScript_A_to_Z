/* 
    Arithmetic Operator
        + - * / % ++ --

*/

var a = 10
var b = 10

// The addition operator (+) adds numbers
console.log(a + b)

// The Subtraction operator (-) subtracts numbers
console.log(a - b)

// The multiplication operator (*) multiplies numbers
console.log(a * b)

// The division operator (/) divides numbers
console.log(a / b)

// The modulus operator (%) returns the division remainder
console.log(a % b)

// The increment operator (++) increments numbers.
console.log(a++)
console.log(a)

// Pre incremental
console.log(++a)
// The decrement operator (--) decrements numbers.
console.log(a--)
console.log(a)

// Pre decremental
console.log(--a)

/*  Assignment Operator
         +=, -=, /=, %=, **=

*/
var m = 20
var n = 10

// The += assignment operator adds a value to a variable
m += n
console.log(m)

// The -= assignment operator subtracts a value from a variable.
m -= n
console.log(m)

// The %= assignment operator assigns a remainder to a variable.
m %= n
console.log(m)

/*  Comparison Operator
         <,>, ==, >=, <=, ===, !=
         * It is always return result is True/False

*/

var g = 10
var h = 20

console.log(g == h) // equal
console.log(g >= h) // greater than or equal to
console.log(g <= h) // less than or equal to
console.log(g > h) // greater than
console.log(g < h) // less than
console.log(g != h) // not equal

console.log(g === h) // equal value and equal type 

console.log(g !== h) // not equal value or not equal type

