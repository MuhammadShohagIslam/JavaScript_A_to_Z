/*
    If we want to access every function, we have to put 'export' before every function, in this case, it's accessiable from the outside.

*/

export const add = (a, b)=>{
    return a + b;
}

export const substraction = (a, b)=>{
    return a - b;
}

export const multiplication = (a, b)=>{
    return a * b;
}

export const division = (a, b)=>{
    return a / b;
}